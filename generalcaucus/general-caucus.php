        <h2>Attendance</h2>
        <p>
            Any registered voter may attend their precinct caucus. All public officials must be
            selected through the General Caucus process, and caucus-goers may choose delegates and
            office holders only from those physically present at their caucus meeting.
        </p>

        <h2>Caucusing</h2>
        <p>
            At the time of the precinct caucus, once all caucus-goers have checked in, the offices in
            question are announced. It is then the responsibility of the caucus-goers to deliberate the
            issues surrounding a particular office and to group themselves with someone present to
            represent their precinct and interests. If a group consists of a simple majority of the
            attending caucus, that person becomes the caucus delegate and advances to the following
            caucus for that office. If no group achieves a simple majority, the smallest group is asked
            to disband and join the others. This continues until one group reaches the simple-majority
            threshold.
        </p>
        <p>
            Ideally, debate at each caucus revolves around the specific office being filled. Caucusing
            for a local mayor should spur debate over crime, sanitation, and other local issues;
            caucusing for a senator should focus on international relations, interstate commerce, and
            other federal matters.
        </p>

        <h2>Caucus Structure</h2>
        <p>
            The General Caucus can be used for offices as small as a city council seat or as large as
            governor or senator. A small office may require only two tiers &mdash; a precinct caucus
            followed by a caucus covering the city council district &mdash; while a statewide office
            requires three: precinct, state house-district, and statewide. The middle series of
            caucuses could be conducted by county, as the Iowa caucus currently does, but holding them
            by state legislature district is recommended because those districts are based on
            population and are of reasonably equal size.
        </p>
        <p>
            The secretaries of state and county clerks are responsible for structuring the General
            Caucus efficiently in each state. Their primary task is to calculate the number of tiers
            needed to fill a given office and the size of each caucus.
        </p>
        <p>
            Iowa demonstrates the three-tier structure with its 1,681 precincts and 100 state
            house-districts: each precinct sends one delegate to its state house-district caucus,
            creating groups of roughly 168 delegates, who each select one representative for the final
            statewide caucus of 100 people. The system scales effectively even for larger states.
            California, with its 22,000 precincts and 80 state house-districts, would average about 275
            participants per state house-district caucus at one delegate per precinct. Each of those
            caucuses could send one or two delegates to the statewide caucus, creating a final assembly
            of 80 to 160 people.
        </p>

        <h2>Caucus Sizes</h2>
        <p>
            The size of each caucus is crucial for efficient deliberation. Large gatherings present
            several challenges: communication between participants and potential delegates becomes
            difficult, policy discussion grows inefficient, and voting drags on because of the number
            of potential delegates. These extended sessions can lead to voter fatigue, which may reduce
            future turnout at the precinct level.
        </p>
        <p>
            Smaller groups avoid these inefficiencies, but having too many small caucuses could
            lengthen the overall selection process. A gathering of two to three hundred participants
            should be optimal for conducting business efficiently.
        </p>
        <p>
            Expected turnout must also be considered when drawing precincts. Since participation rarely
            reaches 100%, administrators should size precincts based on projected attendance. If 200
            participants is ideal and typical turnout is 50%, precincts should be drawn to include
            roughly 400 registered voters.
        </p>

        <h2>Multiple Seats</h2>
        <p>
            The General Caucus can also fill offices with multiple seats, such as members of the House
            of Representatives or electors to the Electoral College. One option is to use a single
            congressional-district or elector-district caucus to determine each office, following the
            same process described above. Another is to use a single statewide caucus to determine all
            of a state's representatives or electors at once; this would require a proportional method
            such as the Hamilton Method in the final caucus to divide the seats among different
            factions. As a means of decentralization, however, separate district caucuses are
            recommended.
        </p>

        <h2>Why It Works</h2>
        <h3>It eliminates money in politics</h3>
        <p>
            The General Caucus is a series of meetings among caucus-goers and delegates, and it does
            not need money to operate. There are no individual campaigns requiring vast sums to sway
            voters with advertising. It is not an election &mdash; it is a caucus.
        </p>
        <h3>It removes political parties</h3>
        <p>
            The influence of the parties is severely reduced. Any potential office holder must pass
            through the precinct caucus, the state house-district caucus, and the final statewide
            caucus. Even if a party nominates a candidate, that nominee must attend and be chosen at
            all three.
        </p>
        <h3>It mitigates media influence</h3>
        <p>
            Because millions of people participate at the precinct level, with thousands of potential
            delegates advancing to the district caucuses, neither legacy nor social media can
            concentrate its power on any particular candidate. Voters engage directly with one another
            rather than being steered by media narratives that too often prioritize sensationalism over
            substance &mdash; which in turn strengthens voter independence.
        </p>

