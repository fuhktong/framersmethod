        <h1 class="caucus-title">The General Caucus</h1>
        <p class="caucus-lede">A new election system designed to defeat tyranny and oligarchy.</p>
    </header>

    <div class="caucus-body">
        <p>
            The General Caucus is a series of tiered meetings for selecting public officials to
            specific offices. It begins at the precinct level, where registered voters gather to
            choose delegates for the next caucus. At each precinct caucus, attendees debate the
            relevant issues and group themselves around a potential delegate until one group achieves
            a simple majority. All office holders must come from the caucuses, and state legislature
            districts are used as the basis for the tiers because they are drawn to be of roughly
            equal population.
        </p>
        <p>
            Precinct caucuses select delegates for every office on the ballot that year &mdash;
            governor, senator, representative, elector, and so on &mdash; but each subsequent caucus
            is separated by office. There may be a final statewide caucus for an office like governor
            or senator, or regional caucuses for offices like representative or elector. Discussion at
            each tier focuses on the issues relevant to the office being filled: local matters for a
            mayoral selection, federal issues for a senatorial one.
        </p>
        <p>
            Ultimately, the General Caucus is a system that defends the people from both the perverse
            interests of oligarchs and the populist rhetoric of tyrannically-minded candidates.
        </p>

        <h2>How It Works</h2>
        <p>
            The starting point is the precinct caucus, followed by a state house-district caucus, and
            finally a statewide caucus. State house-districts are recommended for the middle tier
            because they are drawn to be of roughly equal size. The final statewide caucus selects the
            public official.
        </p>
        <p>
            A precinct caucus chooses delegates for as many offices as have been assigned that year
            &mdash; it might choose delegates for mayor, member of Congress, governor, elector, and
            senator all at once. Every subsequent caucus, however, works on only a single office. For
            example, a precinct caucus may choose a delegate for governor; that delegate then meets at
            the state house-district caucus with all the other governor delegates, who together choose
            one delegate to advance. At the final statewide caucus, those delegates meet and choose the
            state's governor. If a senator is being chosen the same year, the senatorial delegates hold
            their own separate caucuses.
        </p>
