        <h1 class="electors-title">An Electors' Convention</h1>
        <p class="electors-lede">Deliberation will give America the president it needs for the future.</p>
    </header>

    <div class="electors-body">
        <p>
            After the several states choose their electors, an Electors' Convention chooses the next
            president. In this environment, populism and tyranny are impossible: to become the next
            president, one of the electors will need the skills of negotiation and compromise.
        </p>
        <p>
            The convention works because of how the electors are chosen in the first place. Under the
            Hamilton Method, each state allocates its electors proportionally, so no single party is
            handed a national majority. With no majority to fall back on, the electors are forced to
            deliberate.
        </p>

        <h2>The Hamilton Method</h2>
        <p>
            The Hamilton Method splits up a state's electors by first giving each party the whole
            number of electors its share of the vote earns, and then awarding any leftover seats to the
            parties with the largest remainders. It is proportional representation with a touch of
            competition for the final seats.
        </p>
        <figure class="electors-figure">
            <img src="/images/hamiltonmethodequation.png" alt="The Hamilton Method equation" />
            <figcaption>The Hamilton Method.</figcaption>
        </figure>
        <p>
            Because electors are chosen locally and proportionally, political influence is dispersed
            across the country. With thousands of possible electors, no elector candidate can wield
            concentrated power on the national stage, and money, mass media, and foreign intelligence
            services have little ability to sway the result. The single national contest that populist
            and tyrannical rhetoric depends on is effectively eliminated.
        </p>

        <h2>No Majority</h2>
        <p>
            When the electors assemble at the seat of government, the Hamilton Method will have
            prevented any party from achieving a majority of the electoral vote. The electors are
            therefore forced into deliberation. To be chosen as the next president, one of them must
            build a coalition of 270 electors through negotiation and compromise.
        </p>
        <figure class="electors-figure">
            <img src="/images/capitolwithelectors.png" alt="Electors assembled at the Capitol" />
            <figcaption>Electors assemble to choose the president.</figcaption>
        </figure>
        <p>
            This is the heart of the system: a president emerges not from a national popularity contest
            but from a deliberative body that must reach consensus. Populism and tyranny, which rely on
            swaying a mass electorate, have no purchase in a room that runs on coalition-building.
        </p>

        <h2>Key Points</h2>
        <ul class="electors-list">
            <li>Elections are local within each state; the national presidential election is eliminated.</li>
            <li>Electors are allocated proportionally by the Hamilton Method, so no party is handed a majority.</li>
            <li>The convention creates a deliberative environment where compromise and negotiation are required.</li>
            <li>Populism and tyranny are ineffective.</li>
            <li>No money is required to hold the convention; campaigning for elector remains, but it is decentralized.</li>
            <li>With thousands of potential electors, foreign intelligence services, mass media, and social media cannot concentrate their influence.</li>
        </ul>
