            <h1>Caucusing</h1>
            <p>
                At the time of the precinct caucus, with all caucus-goers checked
                in, the offices in question are announced. It is then the
                responsibility of the caucus-goers to deliberate the issues
                regarding the particular office and then group themselves with
                someone from the caucus to represent their precinct and interests.
                If a group consists of a simple majority of the attending caucus,
                that person is the caucus delegate and will attend the following
                caucus for that specific office. If no group at the caucus
                achieves a simple majority, the smallest group is asked to disband
                and join other groups. This process continues until one group
                achieves the simple majority requirement.
            </p>
            <p>
                Ideally, debate at each caucus will revolve around the particular
                office the caucus-goers are choosing delegates for. For example,
