            <h1>Caucus Sizes</h1>
            <p>
                The size of each caucus is crucial for efficient deliberation.
                Large gatherings present several challenges: difficult
                communication between participants and potential delegates,
                inefficient policy discussions, and time-consuming voting
                processes due to numerous potential delegates. These extended
                sessions can lead to voter fatigue, potentially reducing future
                turnout at the precinct level.
            </p>
            <p>
                While smaller groups avoid these inefficiencies, having too many
                small caucuses could lengthen the overall selection process. A
                gathering of two to three hundred participants should be optimal
                for conducting business efficiently.
            </p>
            <p>
                Expected turnout must be considered when determining precinct
                sizes. Since participation rarely reaches 100%, election
                administrators should design precincts based on projected
                attendance. For example, if 200 participants is ideal and typical
                turnout is 50%, precincts should be drawn to include approximately
                400 registered voters.
            </p>
        </div>
    </section>

