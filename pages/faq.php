        <h1 class="faq-section-title">Answers</h1>

        <h2 id="a-section">Electoral College</h2>

        <h3 id="a1">What is the Electoral College?</h3>
        <p>The Electoral College is the process used to determine the next president of the United States.</p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="a2">What does the Electoral College do?</h3>
        <p>
            The Electoral College gives each state a number of electors, based on population, to choose
            the next president of the United States.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="a3">What does the Electoral College mean?</h3>
        <p>
            The Electoral College is a reference for how the United States chooses the next president. It
            is often used to distinguish the process that is different from the popular vote method, which
            the United States does not use to choose the president.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="a4">What are electoral votes?</h3>
        <p>Electoral votes are individual votes cast by electors throughout the several states.</p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="a5">Does the Electoral College elect the president?</h3>
        <p>Yes, the Electoral College elects the president of the United States.</p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="a6">Why is the Electoral College important?</h3>
        <p>
            The Electoral College is important because it is the constitutional process to choose the next
            president of the United States.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="a7">Why does the Electoral College exist?</h3>
        <p>The Electoral College exists to select the president of the United States.</p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h2 id="b-section">Electors</h2>

        <h3 id="b1">What are the Electoral College electors?</h3>
        <p>
            The electors in the Electoral College are individuals who cast ballots in their home states
            for the next president of the United States.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="b2">Who elects the Electoral College?</h3>
        <p>
            The Constitution determines that each state shall decide how electors are chosen. However, in
            practice, two groups elect the members of the Electoral College. First, parties within each
            state will choose their slate of electors. Depending on the state, these slates of electors
            may be chosen through a party convention, from the state's central committee, or from the
            presidential campaign itself.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="b3">Are members of the Electoral College anonymous?</h3>
        <p>
            No, the members of the Electoral College are not anonymous. Their names are on party slates
            before the election and their votes for president are public record.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="b4">Are members of the Electoral College paid?</h3>
        <p>
            Electors are not paid but usually receive some type of stipend to cover the cost of travel to
            the state capital.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="b5">Are members of the Electoral College elected?</h3>
        <p>
            Yes, the members of the Electoral College are elected in each state, however, voters typically
            cast ballots for a presidential candidate instead of voting directly for individual electors.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="b6">Who chooses the members of the Electoral College?</h3>
        <p>
            The states choose the members of the Electoral College through statewide popular votes.
            Nebraska and Maine choose electors through popular votes in congressional districts and the
            remaining two electors through statewide popular votes.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h2 id="c-section">Electoral College Voting</h2>

        <h3 id="c1">What if the Electoral College has a tie vote? Can the Electoral College tie?</h3>
        <p>
            If the Electoral College results in a tie vote, then the process to choose the next president
            moves to the House of Representatives. In the House, each state delegation will have one vote
            to choose the next president from a list of the top three electoral vote recipients until one
            candidate receives a majority.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="c2">What if the Electoral College doesn't achieve a majority vote?</h3>
        <p>
            If the Electoral College results in no presidential candidates receiving a majority vote, then
            the process to choose the next president moves to the House of Representatives. In the House,
            each state delegation will have one vote to choose the next president from a list of the top
            three electoral vote recipients until one candidate receives a majority.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="c3">Who breaks an Electoral College tie?</h3>
        <p>
            In the event of a tie in the Electoral College, the House of Representatives will choose the
            next president by state delegation. For example, in 2024, the state of Michigan has thirteen
            representatives, seven Democrats and six Republicans. Assuming the seven Democrats vote for
            their party, the state of Michigan will award the Democratic presidential nominee one vote. To
            win, someone must receive an absolute majority, or 26 state-delegation votes, to become
            president.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h2 id="d-section">Origin</h2>

        <h3 id="d1">Who created the Electoral College?</h3>
        <p>
            The Electoral College was created by the framers of the Constitution at Independence Hall in
            Philadelphia, Pennsylvania in 1787.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="d2">When was the Electoral College first used? When was the Electoral College started?</h3>
        <p>
            The first Electoral College was used on February 4, 1789, to choose George Washington as the
            first president of the United States.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="d3">Why was the Electoral College created?</h3>
        <p>
            The Electoral College was created after debating three different methods to choose the
            president: the legislature method, the electors' convention method, and the popular vote. The
            framers ended up compromising by creating a hybrid method of the legislature and electors'
            convention methods. The popular vote was highly unpopular with the framers at the
            Constitutional Convention.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="d4">Where did the Electoral College come from?</h3>
        <p>
            The Electoral College came from a series of debates during the Constitutional Convention of
            1787. After debating the legislature method and the electors' convention method, the framers
            compromised and blended the two ideas.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="d5">Where is the Electoral College in the Constitution?</h3>
        <p>
            The language related to the Electoral College may be found in Article II, Section I, Clause 2
            &amp; 3 of the U.S. Constitution. The full text may be found in the
            <a href="https://www.archives.gov/founding-docs/constitution-transcript#2-1" target="_blank" rel="noreferrer">archives</a>.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h2 id="e-section">Calendar</h2>

        <h3 id="e1">When is the Electoral College used? When does the Electoral College meet?</h3>
        <p>
            The electors of the Electoral College will assemble in their respective state capitals on the
            first Tuesday after the second Wednesday in December after the general election. On this day,
            the electors will cast their ballots and then mail them to Congress. In 2024, this day falls
            on December 17.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="e2">When are the members of the Electoral College chosen?</h3>
        <p>
            The members of the Electoral College, also known as electors, are chosen in two steps. First,
            the political parties of the presidential candidates will choose slates of electors through a
            party convention, by a party's state central committee, or by the presidential campaign
            itself. Then, whichever presidential candidate wins a state's popular vote will determine
            which slate of electors will be able to vote for president.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="e3">Where does the Electoral College meet? Where do Electoral College electors vote?</h3>
        <p>
            The electors of the Electoral College meet in the state capitals of their respective states to
            cast ballots for the president.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h2 id="f-section">Constitution</h2>

        <h3 id="f1">Why is the Electoral College necessary to choose the president?</h3>
        <p>
            The Electoral College is necessary to choose the president because it derives from Article II
            of the U.S. Constitution.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="f2">Which amendment created the Electoral College?</h3>
        <p>
            The Electoral College was written into the original Constitution, however, the Twelfth
            Amendment modified the process to create today's version of choosing the president.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="f3">Can the Electoral College be reformed?</h3>
        <p>
            Yes, the Electoral College can be reformed. Just like any other aspect of the U.S.
            Constitution, it can be altered or removed through an amendment to the Constitution.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h2 id="g-section">Operation</h2>

        <h3 id="g1">How does the Electoral College work?</h3>
        <p>
            The Electoral College used today works by candidates competing to win the popular vote in
            individual states to win each state's respective electoral votes. Whichever candidate receives
            270 or more electoral votes will win the presidential election.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="g2">How are electoral votes determined for each state?</h3>
        <p>
            Electoral votes are determined by the number of representatives each state has been allotted
            in the House of Representatives plus its two senators. For example, Michigan has 13
            representatives and 2 senators, giving them a total of 15 electors.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="g3">What's the simplest way to explain the Electoral College?</h3>
        <p>
            The most simple way to explain the Electoral College is a candidate must win 270 electoral
            votes or more to win the presidential election.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="g4">Are Electoral College votes based on population?</h3>
        <p>
            Yes, the electoral votes used in the Electoral College are based on population. Each state is
            given representatives in the House of Representatives based on population.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h2 id="h-section">Faithless Electors</h2>

        <h3 id="h1">Can members of the Electoral College change their mind? Can members of the Electoral College go rogue?</h3>
        <p>
            Yes, electors of the Electoral College can vote differently from the results of their state's
            popular vote total. They are often referred to as faithless electors. It's difficult to know
            the number from past elections because the criteria may be different for counting faithless
            electors. That being said, faithless electors have never overturned a presidential election.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="h2">When has the Electoral College voted differently than the popular vote?</h3>
        <p>
            The Electoral College vote has been different than the popular vote five times in U.S.
            history. This occurred in the 1824, 1876, 1888, 2000, and 2016 elections.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h2 id="i-section">Miscellaneous</h2>

        <h3 id="i1">Is the Electoral College capitalized?</h3>
        <p>Yes, the Electoral College is capitalized.</p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="i2">Can Electoral College votes be split?</h3>
        <p>No, electoral votes in the Electoral College cannot be split.</p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="i3">Where is the Electoral College located?</h3>
        <p>The Electoral College is located in every state capital and Washington, D.C.</p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="i4">Why is the Electoral College different from the popular vote?</h3>
        <p>
            The Electoral College is different from the popular vote because the vote to choose the
            president is the result of counting electoral votes rather than votes by individual voters.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

        <h3 id="i5">What are the pros and cons of the Electoral College?</h3>
        <p>
            The Electoral College is a very controversial institution, so the pros and cons may differ
            from person to person. However, broadly speaking, an often-cited advantage is that presidential
            candidates must win states throughout the country, for example, more rural states, instead of
            population centers. The most often disadvantage discussed is that the candidate with the most
            votes does not determine the winner.
        </p>
        <a class="faq-top" href="#top">Return to Top</a>

